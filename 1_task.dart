void main() {
  // –ó–∞–≤–¥–∞–Ω–Ω—è 1: –ú–∞—Ç–µ–º–∞—Ç–∏—á–Ω—ñ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—ñ –¥–æ–≤–∂–∏–Ω–∞ —ñ —à–∏—Ä–∏–Ω–∞ –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞ (—à–∏—Ä–∏–Ω–∞ 18.4, –≤–∏—Å–æ—Ç–∞ 30.9)
  // –î–µ width - —à–∏—Ä–∏–Ω–∞, height - –≤–∏—Å–æ—Ç–∞.
  // 2) –û–±—á–∏—Å–ª—ñ—Ç—å –ø–ª–æ—â—É —ñ –ø–µ—Ä–∏–º–µ—Ç—Ä –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞.
  // –î–µ p - –ø–µ—Ä–∏–º–µ—Ç—Ä, s - –ø–ª–æ—â–∞.
  // 3) –í–∏–≤–µ–¥—ñ—Ç—å –ø–ª–æ—â—É —ñ –ø–µ—Ä–∏–º–µ—Ç—Ä.
  // 4) –í–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ –æ–ø–µ—Ä–∞—Ç–æ—Ä ~/ –¥–ª—è –æ–±—á–∏—Å–ª–µ–Ω–Ω—è —Å–∫—ñ–ª—å–∫–∏ –ø–æ–≤–Ω–∏—Ö –∫–≤–∞–¥—Ä–∞—Ç—ñ–≤ 1—Ö1 –≤–º—ñ—â–∞—î—Ç—å—Å—è —É –ø–ª–æ—â—É.
  // –î–µ squares - –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø–æ–≤–Ω–∏—Ö –∫–≤–∞–¥—Ä–∞—Ç—ñ–≤ 1—Ö1 –≤–º—ñ—â—É—î—Ç—å—Å—è —É –ø–ª–æ—â—É –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞.

  double width = 18.4;
  double height = 30.9;

  double p = 2 * (width + height);
  double s = width * height;

  print('p = $p');
  print('s = $s');
  print('squares = ${s ~/ 1}');

  // –ó–∞–≤–¥–∞–Ω–Ω—è 2: –û–ø–µ—Ä–∞—Ç–æ—Ä–∏ –ø–æ—Ä—ñ–≤–Ω—è–Ω–Ω—è
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—É number.
  // 2) –ü–µ—Ä–µ–≤—ñ—Ä—Ç–µ:
  // –ß–∏ –±—ñ–ª—å—à–µ —á–∏—Å–ª–æ –∑–∞ 50.
  // –ß–∏ –º–µ–Ω—à–µ —á–∏—Å–ª–æ –∑–∞ 100.
  // –ß–∏ –¥—ñ–ª–∏—Ç—å—Å—è —á–∏—Å–ª–æ –Ω–∞ 5 –±–µ–∑ –∑–∞–ª–∏—à–∫—É.
  // 3) –í–∏–≤–µ–¥—ñ—Ç—å –≤—Å—ñ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏.

  int number = 25;
  bool isGreaterThan50 = number > 50;
  bool isLessThan100 = number < 100;
  bool isDivisibleBy5 = number % 5 == 0;

  print('Is the number greater than 50? = $isGreaterThan50');
  print('Is the number less than 100? = $isLessThan100');
  print('Is the number divisible by 5? = $isDivisibleBy5');

  // –ó–∞–≤–¥–∞–Ω–Ω—è 3: –õ–æ–≥—ñ—á–Ω—ñ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—ñ hasMoney —ñ isStoreOpen (—Ç–∏–ø—É bool).
  // 2) –ü–µ—Ä–µ–≤—ñ—Ä—Ç–µ:
  // –ß–∏ –º–æ–∂–Ω–∞ –∑—Ä–æ–±–∏—Ç–∏ –ø–æ–∫—É–ø–∫—É (—è–∫—â–æ —î –≥—Ä–æ—à—ñ —ñ –º–∞–≥–∞–∑–∏–Ω –≤—ñ–¥–∫—Ä–∏—Ç–∏–π).
  // –ß–∏ –ø–æ—Ç—Ä—ñ–±–Ω–æ –ø–æ—á–µ–∫–∞—Ç–∏ (—è–∫—â–æ –º–∞–≥–∞–∑–∏–Ω –∑–∞–∫—Ä–∏—Ç–∏–π –∞–±–æ –Ω–µ–º–∞—î –≥—Ä–æ—à–µ–π).
  // 3) –í–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏ &&, ||, !.

  bool hasMoney = true;
  bool isStoreOpen = false;

  bool canMakePurchase = hasMoney && !isStoreOpen;
  bool needToWait = isStoreOpen || !hasMoney;

  print('Can make purchase? = $canMakePurchase');
  print('Need to wait? = $needToWait');

  // –ó–∞–≤–¥–∞–Ω–Ω—è 4: –¢–µ—Ä–Ω–∞—Ä–Ω–∏–π –æ–ø–µ—Ä–∞—Ç–æ—Ä
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—É temperature.
  // 2) –í–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ —Ç–µ—Ä–Ω–∞—Ä–Ω–∏–π –æ–ø–µ—Ä–∞—Ç–æ—Ä, —â–æ–± –≤–∏–∑–Ω–∞—á–∏—Ç–∏:
  // –Ø–∫—â–æ –±—ñ–ª—å—à–µ 25 ‚Äî ‚Äú–¢–µ–ø–ª–æ‚Äù.
  // –Ø–∫—â–æ –º–µ–Ω—à–µ –∞–±–æ –¥–æ—Ä—ñ–≤–Ω—é—î 25 ‚Äî ‚Äú–ü—Ä–æ—Ö–æ–ª–æ–¥–Ω–æ‚Äù.
  // 3) –ü–æ—Ç—ñ–º —Å—Ç–≤–æ—Ä—ñ—Ç—å —â–µ –æ–¥–∏–Ω —Ç–µ—Ä–Ω–∞—Ä–Ω–∏–π –æ–ø–µ—Ä–∞—Ç–æ—Ä:
  // –Ø–∫—â–æ –º–µ–Ω—à–µ 10 ‚Äî –≤–∏–≤–µ—Å—Ç–∏ ‚Äú–î—É–∂–µ —Ö–æ–ª–æ–¥–Ω–æ‚Äù.

  int temperature = 20;
  String weather = temperature > 25 ? 'Warm' : 'Cool';
  String coldWeather = temperature < 10 ? 'VeryCold' : '';
  print('Weather: $weather');
  if (coldWeather.isNotEmpty) {
    print(coldWeather);
  }
  // –ó–∞–≤–¥–∞–Ω–Ω—è 5: –û–ø–µ—Ä–∞—Ç–æ—Ä–∏ –ø—Ä–∏—Å–≤–æ—î–Ω–Ω—è
  // –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–≤–∞—Ç–∏ —Ç—ñ–ª—å–∫–∏ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∏ –ø—Ä–∏—Å–≤–æ—î–Ω–Ω—è (+=, -=, *=, /=)
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—É score = 0.
  // 2) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–∏ –∑ –¥–∞–Ω–∏–º–∏:
  // correctAnswers = 17;
  // mistakes = 3;
  // totalQuestions = 20;
  // 3) –î–æ–¥–∞–π—Ç–µ 10 –±–∞–ª—ñ–≤ –∑–∞ –∫–æ–∂–Ω—É –ø—Ä–∞–≤–∏–ª—å–Ω—É –≤—ñ–¥–ø–æ–≤—ñ–¥—å.
  // 4) –í—ñ–¥–Ω—ñ–º—ñ—Ç—å 5 –±–∞–ª—ñ–≤ –∑–∞ –∫–æ–∂–Ω—É –ø–æ–º–∏–ª–∫—É.
  // 5) –ó–±—ñ–ª—å—à—ñ—Ç—å –∑–∞–≥–∞–ª—å–Ω—É –∫—ñ–ª—å–∫—ñ—Å—Ç—å –±–∞–ª—ñ–≤ —É 2 —Ä–∞–∑–∏.
  // 6) –ü–æ–¥—ñ–ª—ñ—Ç—å –±–∞–ª–∏ –Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –∑–∞–ø–∏—Ç–∞–Ω—å.
  // 7) –í–∏–≤–µ–¥—ñ—Ç—å –æ—Å—Ç–∞—Ç–æ—á–Ω–∏–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç.

  int score = 0;
  int correctAnswers = 17;
  int mistakes = 3;
  int totalQuestions = 20;
  score += correctAnswers * 10;
  score -= mistakes * 5;
  score *= 2;
  score ~/= totalQuestions;
  print('Final score: $score');

  // –ó–∞–≤–¥–∞–Ω–Ω—è 6: –£–º–æ–≤–Ω—ñ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—ó if/else
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—É examScore –∑ –∑–Ω–∞—á–µ–Ω–Ω—è–º 56
  // 2) –°—Ç–≤–æ—Ä—ñ—Ç—å —É–º–æ–≤–Ω—ñ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—ó if / else if / else
  // 3) –Ø–∫—â–æ –±—ñ–ª—å—à–µ 90 ‚Äî –≤–∏–≤–µ—Å—Ç–∏ ‚Äú–í—ñ–¥–º—ñ–Ω–Ω–æ‚Äù.
  // 4) –Ø–∫—â–æ 75‚Äì90 ‚Äî ‚Äú–î–æ–±—Ä–µ‚Äù.
  // 5) –Ø–∫—â–æ 60‚Äì74 ‚Äî ‚Äú–ó–∞–¥–æ–≤—ñ–ª—å–Ω–æ‚Äù.
  // 6) –Ø–∫—â–æ –º–µ–Ω—à–µ 60 ‚Äî ‚Äú–ù–µ –∑–¥–∞–Ω–æ‚Äù.
  // 7) –î–æ–¥–∞—Ç–∫–æ–≤–æ: —è–∫—â–æ –æ—Ü—ñ–Ω–∫–∞ –º–µ–Ω—à–µ 20 ‚Äî –≤–∏–≤–µ—Å—Ç–∏ ‚Äú–ü–æ–≤—Ç–æ—Ä–∏—Ç–∏ –∫—É—Ä—Å‚Äù.
  // 8) –ü–æ–µ–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç—É–π—Ç–µ, –∑–º—ñ–Ω—é—é—á–∏ –∑–Ω–∞—á–µ–Ω–Ω—è examScore

  int examScore = 56;

  if (examScore > 90) {
    print('Excellent');
  } else if (examScore >= 75 && examScore <= 90) {
    print('Good');
  } else if (examScore >= 60 && examScore < 75) {
    print('Satisfactory');
  } else if (examScore < 60) {
    print('Not passed');
    if (examScore < 20) {
      print('Repeat the course');
    }
  } else {
    print('Invalid score');
  }
  // –ó–∞–≤–¥–∞–Ω–Ω—è 7: –ö–æ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—è switch statement
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—É dayNumber –≤—ñ–¥ 1 –¥–æ 7.
  // 2) –í–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ switch, —â–æ–± –≤–∏–≤–µ—Å—Ç–∏:
  // –ü–æ–Ω–µ–¥—ñ–ª–æ–∫, –í—ñ–≤—Ç–æ—Ä–æ–∫, ‚Ä¶, –ù–µ–¥—ñ–ª—è.
  // 3) –Ø–∫—â–æ –¥–µ–Ω—å –Ω–µ –≤—ñ–¥ 1 –¥–æ 7 ‚Äî –≤–∏–≤–µ—Å—Ç–∏ ‚Äú–ù–µ–≤—ñ—Ä–Ω–∏–π –Ω–æ–º–µ—Ä –¥–Ω—è‚Äù.
  // 4) –î–æ–¥–∞—Ç–∫–æ–≤–æ: –¥–ª—è –≤–∏—Ö—ñ–¥–Ω–∏—Ö (6, 7) –≤–∏–≤–µ—Å—Ç–∏ ‚Äú–í–∏—Ö—ñ–¥–Ω–∏–π –¥–µ–Ω—å üéâ‚Äù.

  int dayNumber = 7;

  switch (dayNumber) {
    case 1:
      print('Monday');
      break;
    case 2:
      print('Tuesday');
      break;
    case 3:
      print('Wednesday');
      break;
    case 4:
      print('Thursday');
      break;
    case 5:
      print('Friday');
      break;
    case 6:
      print('Saturday');
      print('Weekend day');
      break;
    case 7:
      print('Sunday');
      print('Weekend');
      break;
    default:
      print('Invalid day number');
  }

  // –ó–∞–≤–¥–∞–Ω–Ω—è 8: –¶–∏–∫–ª–∏ for
  // 1) –í–∏–≤–µ–¥—ñ—Ç—å –≤—Å—ñ —á–∏—Å–ª–∞ –≤—ñ–¥ 1 –¥–æ 10.
  // 2) –í–∏–≤–µ–¥—ñ—Ç—å —Ç—ñ–ª—å–∫–∏ –ø–∞—Ä–Ω—ñ —á–∏—Å–ª–∞.
  // 3) –û–±—á–∏—Å–ª—ñ—Ç—å —Å—É–º—É —á–∏—Å–µ–ª –≤—ñ–¥ 1 –¥–æ 10.

  int sum = 0;
  for (int i = 1; i <= 10; i++) {
    print(i);
    if (i % 2 == 0) {
      print('Even number: $i');
    }
    sum += i;
  }
  print('Sum of numbers from 1 to 10: $sum');

  // –ó–∞–≤–¥–∞–Ω–Ω—è 9: –¶–∏–∫–ª while
  // 1) –ü–æ—á–Ω—ñ—Ç—å —ñ–∑ —á–∏—Å–ª–∞ 10.
  // 2) –í–∏–≤–æ–¥—å—Ç–µ —á–∏—Å–ª–∞, –∑–º–µ–Ω—à—É—é—á–∏ —ó—Ö –Ω–∞ 1, –ø–æ–∫–∏ –Ω–µ –¥–æ—Å—è–≥–Ω–µ—Ç–µ 0.
  // 3) –Ø–∫—â–æ —á–∏—Å–ª–æ –ø–∞—Ä–Ω–µ ‚Äî –≤–∏–≤–µ—Å—Ç–∏ ‚Äú–ü–∞—Ä–Ω–µ —á–∏—Å–ª–æ: ‚Ä¶‚Äù, —è–∫—â–æ –Ω–µ–ø–∞—Ä–Ω–µ ‚Äî ‚Äú–ù–µ–ø–∞—Ä–Ω–µ —á–∏—Å–ª–æ: ‚Ä¶‚Äù.

  int counter = 10;
  while (counter >= 0) {
    if (counter % 2 == 0) {
      print('Even counter: $counter');
    } else {
      print('Odd counter: $counter');
    }
    counter--;
  }

  // –ó–∞–≤–¥–∞–Ω–Ω—è 10: Break —ñ Continue
  // 1) –°—Ç–≤–æ—Ä—ñ—Ç—å —Ü–∏–∫–ª for –≤—ñ–¥ 1 –¥–æ 20.
  // 2) –Ø–∫—â–æ —á–∏—Å–ª–æ –¥—ñ–ª–∏—Ç—å—Å—è –Ω–∞ 3 –±–µ–∑ –∑–∞–ª–∏—à–∫—É ‚Äî –ø—Ä–æ–ø—É—Å—Ç—ñ—Ç—å –π–æ–≥–æ (continue).
  // 3) –Ø–∫—â–æ —á–∏—Å–ª–æ –±—ñ–ª—å—à–µ 15 ‚Äî –∑—É–ø–∏–Ω—ñ—Ç—å —Ü–∏–∫–ª (break).
  // –í–∏–≤–µ–¥—ñ—Ç—å —Ç—ñ–ª—å–∫–∏ —Ç—ñ —á–∏—Å–ª–∞, —è–∫—ñ –ø—Ä–æ–π—à–ª–∏ –æ–±–∏–¥–≤—ñ –ø–µ—Ä–µ–≤—ñ—Ä–∫–∏.

  for (int i = 1; i <= 20; i++) {
    if (i % 3 == 0) {
      continue;
    }
    if (i > 15) {
      break;
    }
    print('Number: $i');
  }
}
